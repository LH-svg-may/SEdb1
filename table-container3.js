//。bed文件转换为csv文件
function convertBEDtoCSV(bedContent) {
    const lines = bedContent.split('\n').filter(line => line.trim() !== '' && !line.startsWith('#'));
    const csvLines = lines.map(line => {
        const fields = line.split('\t');
        // 将BED字段转换为CSV行
        return fields.join(',');
    });

    // 添加CSV表头
    const headers = [
        'Overlap show','SE_ID', 'SE_Region', 'Species', 'Sample_ID', 'Tissue_type',
        'Biosample_name', 'Gene', 'Rank', 'Element', 'Common_SNP',
        'eQTL', 'Risk_SNP', 'TFBS', 'CRISRP_Cas9'
    ].join(',');

    return [headers, ...csvLines].join('\n');
}

